        return UIColor(red: 0.996, green: 0.541, blue: 0.502, alpha: 1)
    }
    
    static var imlocalBackgroundColor: UIColor {
        return UIColor(red: 0.988, green: 0.969, blue: 0.957, alpha: 1)
    }
    
    static var imlocalLightGray: UIColor {
        return UIColor(red: 0.562, green: 0.562, blue: 0.562, alpha: 1)
    }
    
    static var shadowColor: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.15)
    }

}
